; usage:
;    rgb   #F8F888, #000000, #10A840, #F8B888
; outputs:
;    db   $FF, $47, $00, $00, $A2, $22, $FF, $46
macro rgb
    REPT _NARG
      REDEF eval EQUS STRRPL("\1", "#", "$")
      REDEF arg EQU {eval}
      REDEF red EQU ((arg & $FF0000) >> 16) / 8
      REDEF grn EQU ((arg & $00FF00) >>  8) / 8
      REDEF blu EQU ((arg & $0000FF) >>  0) / 8
      dw (red) + (grn) << 5 + (blu) << 10
      SHIFT 1
    ENDR
endm

